describe('Meal suggestion', () => {
  beforeEach(() => {
    if (Cypress.env('environment') === 'prod') {
      cy.visit('https://meal-suggestion.s3.eu-central-1.amazonaws.com/index.html')
    } else {
      cy.visit('./src/index.html')
    }
    cy.title().should('be.equal', 'Sugest茫o de Refei莽茫o Vegana')
    cy.contains('h1', 'Refei莽茫o vegana ')
      .as('heading')
      .should('be.visible')
    cy.contains('#meal-name', 'Refei莽茫o: ')
      .as('mealName')
      .should('be.visible')
    cy.contains('#ingredients-label', 'Ingredientes:')
      .as('ingredientsLabel')
      .should('be.visible')
    cy.get('ul li')
      .as('ingredients')
      .should('be.visible')
      .its('length')
      .should('be.at.least', 1)
    cy.contains('button', 'Outra sugest茫o')
      .as('button')
      .should('be.visible')
    cy.get('body').should('have.css', 'background-color', 'rgb(254, 246, 228)')
  })

  it('shows one of all meal suggestions', () => {
    cy.get('@button')
      .click()
      .blur()

    cy.get('@mealName').should('be.visible')
    cy.get('@ingredientsLabel').should('be.visible')
    cy.get('@ingredients')
      .should('be.visible')
      .its('length')
      .should('be.at.least', 1)
  })

  it('shows a filtered meal suggestion', () => {
    cy.get('select#meal-type-filter').select('Saladas')

    cy.get('@mealName')
      .should('be.visible')
      .and('contain', ' (salada)')
    cy.get('@ingredientsLabel').should('be.visible')
    cy.get('@ingredients')
      .should('be.visible')
      .its('length')
      .should('be.at.least', 1)
  })

  it('searches for a meal', () => {
    cy.get('#search-field')
      .type('Ramen{enter}')
      .blur()

    cy.get('@mealName')
      .should('be.visible')
      .and('contain', 'Ramen (sopa)')
    cy.get('@ingredientsLabel').should('be.visible')
    cy.get('@ingredients')
      .should('be.visible')
      .and('contain', 'massa de ramen')
      .and('contain', 'br贸colis')
      .and('contain', 'cenoura')
      .and('contain', 'edamame')
      .and('contain', 'tofu')
      .and('contain', 'cebolinha')
      .and('contain', 'mis么')
      .and('contain', 'milho')
      .its('length')
      .should('equal', 8)
  })

  it('searches for a meal by part of its name', () => {
    cy.get('#search-field')
      .type('Ram{enter}')
      .blur()

    cy.get('@mealName')
      .should('be.visible')
      .and('contain', 'Ramen (sopa)')
    cy.get('@ingredientsLabel').should('be.visible')
    cy.get('@ingredients')
      .should('be.visible')
      .and('contain', 'massa de ramen')
      .and('contain', 'br贸colis')
      .and('contain', 'cenoura')
      .and('contain', 'edamame')
      .and('contain', 'tofu')
      .and('contain', 'cebolinha')
      .and('contain', 'mis么')
      .and('contain', 'milho')
      .its('length')
      .should('equal', 8)
  })

  it('filters and searches for a meal', () => {
    cy.get('select#meal-type-filter').select('Sopas')
    cy.get('#search-field')
      .type('Ramen{enter}')
      .blur()

    cy.get('@mealName')
      .should('be.visible')
      .and('contain', 'Ramen (sopa)')
    cy.get('@ingredientsLabel').should('be.visible')
    cy.get('@ingredients')
      .should('be.visible')
      .and('contain', 'massa de ramen')
      .and('contain', 'br贸colis')
      .and('contain', 'cenoura')
      .and('contain', 'edamame')
      .and('contain', 'tofu')
      .and('contain', 'cebolinha')
      .and('contain', 'mis么')
      .and('contain', 'milho')
      .its('length')
      .should('equal', 8)
  })

  it('filters and searches for a meal (negative scenario)', () => {
    cy.get('select#meal-type-filter').select('Sopas')
    cy.get('#search-field')
      .type('Iron Energy{enter}')
      .blur()

    cy.get('@mealName')
      .should('be.visible')
      .and('contain', ' (sopa)')
    cy.get('@ingredientsLabel').should('be.visible')
    cy.get('@ingredients')
      .should('be.visible')
      .its('length')
      .should('be.at.least', 1)
  })

  it('shows h1 centralized on mobile viewport', { viewportWidth: 400 }, () => {
    cy.get('@heading').should('have.css', 'text-align', 'center')
  })
})
